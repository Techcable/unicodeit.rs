pub type RawTable = &'static [(&'static str, &'static str)];

mod common;

fn do_test_any_symbols_v06(
    symbols: RawTable,
    replace: ReplaceFunc,
    mut transform_latex: impl FnMut(&str) -> String,
    mut transform_unicode: impl FnMut(&str) -> String,
) {
    let do_assert = assert_func!(replace);
    for group in symbols.iter().chunks(20).into_iter() {
        let group = group.copied().collect_vec();
        for &(l, u) in &group {
            // test individually
            do_assert(&transform_latex(l), &transform_unicode(u));
        }
        // test as a group
        let latex = group
            .iter()
            .map(|(l, _)| transform_latex(l))
            .collect::<String>();
        let unicode = group
            .iter()
            .map(|(_, u)| transform_unicode(u))
            .collect::<String>();
        do_assert(&latex, &unicode);
    }
}

fn do_test_symbols_v06(replace: ReplaceFunc) {
    do_test_any_symbols_v06(SYMBOLS_V06, replace, str::to_string, str::to_string)
}

fn do_test_sub_super_scripts_v06(replace: ReplaceFunc) {
    do_test_any_symbols_v06(
        SUB_SUPER_SCRIPTS_V06,
        replace,
        str::to_string,
        str::to_string,
    )
}

